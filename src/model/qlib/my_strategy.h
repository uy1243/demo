    void onMarketUpdate(const MarketUpdateEvent& event);

private:
    EventSystem* event_system_;
    OrderManager* order_manager_;
    std::unordered_map<std::string, DailyPredictions> latest_predictions_;
    std::string last_loaded_file_;
    std::chrono::steady_clock::time_point last_load_time_;

    // Path to the JSON file generated by Python
    std::string prediction_file_path_ = "qlib_predictions.json";

    void updatePredictionsIfNeeded();
    std::string getCurrentDateStr(); // Helper to get YYYY-MM-DD format
    void executeSignal(const std::string& instrument, const PredictionRecord& prediction);
